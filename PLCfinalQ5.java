import java.util.*; 
public class PLCFinalQ5 {
	public static void main(String[] args) {
		
        int n=5;
        int x[][]= new int[n][n];
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++)
                if(x[i][j]!=0)
                break;
        System.out.println("First all-zero row is:" +i+1);
        break;
        }
		 
	}
}
